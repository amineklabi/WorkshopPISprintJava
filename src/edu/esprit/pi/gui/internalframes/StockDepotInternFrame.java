    private void remplirAdresseDepotComboBox() {
        listDepot = depotDAO.findAll();
        for (Depot depot : listDepot) {
            adresseDepotjComboBox1.addItem(depot.getAdresse());
        }
    }

    private void remplirStockComboBox() {
        listStock = stockDAO.findAll();
        for (Stock stock : listStock) {
            libelleVetjComboBox1.addItem(stock.getId());
        }
    }
