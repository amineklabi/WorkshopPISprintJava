    private void miseaJourjButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_miseaJourjButton3ActionPerformed
        stock = new Stock();
        stock.setId(Integer.parseInt(libelleVetjComboBox1.getSelectedItem().toString()));
        stock.setPrix(Double.parseDouble(prixjTextField4.getText()));
        stock.setLibelle(libellejTextField1.getText());
        stock.setQtiteStock(Integer.parseInt(quantitejSpinner1.getValue().toString()));
        if (hommejRadioButton2.isSelected()) {
            stock.setType(hommejRadioButton2.getText());
        } else {
            stock.setType(femmejRadioButton1.getText());
        }
        stock.setDepot(depotDAO.find(adresseDepotjComboBox1.getSelectedItem().toString()));
        stockDAO.update(stock);
    }//GEN-LAST:event_miseaJourjButton3ActionPerformed

