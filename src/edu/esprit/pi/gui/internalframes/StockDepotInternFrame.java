    private void libelleVetjComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_libelleVetjComboBox1ActionPerformed
        Stock s = stockDAO.find(Integer.parseInt(libelleVetjComboBox1.getSelectedItem().toString()));
        libellejTextField1.setText(s.getLibelle());
        prixjTextField4.setText(String.valueOf(s.getPrix()));
        quantitejSpinner1.setValue(s.getQtiteStock());
        adresseDepotjComboBox1.setSelectedItem(s.getDepot().getAdresse());
        if (s.getType().equals("Homme")) {
            hommejRadioButton2.setSelected(true);
            femmejRadioButton1.setSelected(false);
        } else {
            hommejRadioButton2.setSelected(false);
            femmejRadioButton1.setSelected(true);
        }
    }//GEN-LAST:event_libelleVetjComboBox1ActionPerformed

