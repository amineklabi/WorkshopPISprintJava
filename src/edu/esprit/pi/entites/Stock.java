package edu.esprit.pi.entites;

/**
 * @author Amine
 */
public class Stock {
    private int id;
    private String Libelle;
    private String type;
    private int qtiteStock;
    private double prix;
    private Depot depot;
    
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getLibelle() {
        return Libelle;
    }

    public void setLibelle(String Libelle) {
        this.Libelle = Libelle;
    }

    public int getQtiteStock() {
        return qtiteStock;
    }

    public void setQtiteStock(int quantite) {
        this.qtiteStock = quantite;
    }

    public double getPrix() {
        return prix;
    }

    public void setPrix(double prix) {
        this.prix = prix;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public Depot getDepot() {
        return depot;
    }

    public void setDepot(Depot depot) {
        this.depot = depot;
    }

    @Override
    public String toString() {
        return "Stock{" + "id=" + id + ", Libelle=" + Libelle + ", type=" + type + ", quantite=" + qtiteStock + ", prix=" + prix + ", depot=" + depot + '}';
    }

   

    
     
}
